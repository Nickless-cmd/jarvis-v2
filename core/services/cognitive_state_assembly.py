# Cross-process shared cache (2026-05-14): was a per-worker dict, but
# jarvis-api runs 4 workers — hit rate dropped to ~25% with random
# dispatch. Now backed by SQLite via shared_cache so all workers see
# the same cached state. Per-process snapshot kept for the
# state-change-detection fast path (extends cache without a write).
_COHERENT_CACHE_KEY_PREFIX = "cognitive_state:"
_CACHE_INVALIDATION_SNAPSHOT: dict[str, object] = {}


def _cognitive_cache_key(mode_key: str) -> str:
    return f"{_COHERENT_CACHE_KEY_PREFIX}{mode_key}"


def _cache_ttl_seconds() -> float:
    """Read TTL from settings; default 120s. TTL=0 disables caching."""
    try:
        from core.runtime.settings import load_settings
        s = load_settings()
        return float(s.cognitive_state_cache_ttl)
    except Exception:
        return 120.0


def _cache_enabled() -> bool:
    """Check if caching is enabled in settings. TTL=0 also disables."""
    try:
        from core.runtime.settings import load_settings
        s = load_settings()
        if int(s.cognitive_state_cache_ttl) <= 0:
            return False
        return bool(s.cognitive_state_cache_enabled)
    except Exception:
        return True  # default on


def _build_invalidation_snapshot() -> dict[str, object]:
    """Snapshot the key state signals that invalidate the cache.

    If any of these change between calls, the cache is considered stale.
    This does 3 fast DB reads — cheap enough for cache-miss paths,
    but skipped entirely on cache hits (TTL still valid).
    """
    snapshot: dict[str, object] = {}

    # Personality vector: version + bearing + mood fingerprint
    pv = _safe_call(get_latest_cognitive_personality_vector)
    if pv:
        snapshot["pv_version"] = pv.get("version")
        snapshot["pv_bearing"] = str(pv.get("current_bearing") or "")[:80]
        eb = _safe_json(pv.get("emotional_baseline"))
        if eb:
            # Round to 1 decimal to avoid float-noise invalidation
            snapshot["pv_mood_fingerprint"] = tuple(
                round(float(v), 1) for v in eb.values() if isinstance(v, (int, float))
            )
        snapshot["pv_updated_at"] = str(pv.get("updated_at") or "")

    # Chronicle: latest entry timestamp
    chronicle = _safe_call(get_latest_cognitive_chronicle_entry)
    if chronicle:
        snapshot["chronicle_at"] = str(chronicle.get("created_at") or "")

    # Rhythm phase
    rhythm = _safe_call(get_latest_cognitive_rhythm_state)
    if rhythm:
        snapshot["rhythm_phase"] = str(rhythm.get("phase") or "")

    return snapshot


def _is_cache_valid(cache_key: str) -> bool:
    """Check if cached state for `mode_key` (e.g. 'full') is fresh+coherent.

    Two-tier check:
    1. Within TTL → immediate return True (zero DB reads beyond cache lookup)
    2. TTL expired (lazy-expired by shared_cache.get → None) → would have
       returned False already. So this function focuses on Tier 1 only.

    Tier 2 (state-change extension) is now handled in _get_cached_state
    because it needs the actual entry payload, which only get() can fetch.
    """
    from core.services import shared_cache as _sc
    entry = _sc.get(_cognitive_cache_key(cache_key))
    return entry is not None


def _get_cached_state(cache_key: str) -> str | None:
    """Return cached cognitive state string if valid, None otherwise.

    Now reads from shared_cache (SQLite-backed, cross-worker shared).
    The TTL+invalidation-snapshot logic that used to live here is
    preserved: shared_cache handles TTL expiry, and we re-extend the
    entry when the invalidation snapshot matches (state stable).
    """
    if not _cache_enabled():
        return None
    from core.services import shared_cache as _sc
    full_key = _cognitive_cache_key(cache_key)
    entry = _sc.get(full_key)
    if not entry:
        return None
    # Tilstands-bevidst invalidering (2026-06-30): cachen holdt før KUN på TTL —
    # `invalidation_snapshot` blev gemt men aldrig sammenlignet (død maskineri).
    # Nu sammenligner vi de centrale state-signaler (pv-version+bearing+mood,
    # chronicle-timestamp, rhythm-fase) ved HVERT opslag: hvis tilstanden har
    # ændret sig siden cachen blev bygget → stale uanset TTL → behandl som miss
    # (rebuild med frisk indre tilstand). 3 hurtige DB-reads (~ms), forsvindende
    # billigt mod 4-6s LLM. Self-safe: en snapshot-fejl må ALDRIG vælte et hit.
    try:
        stored_snap = entry.get("invalidation_snapshot")
        if isinstance(stored_snap, dict) and stored_snap:
            fresh_snap = _build_invalidation_snapshot()
            if fresh_snap:
                # Sammenlign JSON-normaliseret: shared_cache (SQLite/JSON) konverterer
                # tuples → lists (pv_mood_fingerprint er en tuple), så et rå dict-!= ville
                # ALTID matche falsk-stale → cachen ville aldrig hitte. sort_keys+default=str
                # giver en kanonisk form begge veje.
                import json as _json
                _a = _json.dumps(fresh_snap, sort_keys=True, default=str)
                _b = _json.dumps(stored_snap, sort_keys=True, default=str)
                if _a != _b:
                    logger.info(
                        "cognitive_state_cache: STALE (indre tilstand ændret) %s → rebuild",
                        cache_key,
                    )
                    return None
    except Exception:
        pass
    # Frisk (TTL OK + tilstand uændret) — returnér med det samme
    text = str(entry.get("text") or "")
    logger.debug("cognitive_state_cache: HIT for %s", cache_key)
    return text


def _set_cached_state(cache_key: str, text: str, sources: list[str]) -> None:
    """Store assembled cognitive state in shared_cache (cross-worker)."""
    global _CACHE_INVALIDATION_SNAPSHOT
    if not _cache_enabled():
        return

    # Frisk snapshot ved WRITE-tid: det er den tilstand cachen repræsenterer.
    # Hit-path'en (_get_cached_state) sammenligner mod en frisk snapshot → ægte
    # tilstands-bevidst invalidering på tværs af workers. (Før: en gammel global
    # der aldrig blev opdateret → snapshotten var meningsløs.)
    snap = _build_invalidation_snapshot()
    _CACHE_INVALIDATION_SNAPSHOT = snap

    from core.services import shared_cache as _sc
    ttl = _cache_ttl_seconds()
    _sc.set(
        _cognitive_cache_key(cache_key),
        {
            "text": text,
            "sources": sources,
            "cached_at": datetime.now(UTC).isoformat().replace("+00:00", "Z"),
            "chars": len(text),
            # Tilstanden cachen blev bygget på — sammenlignes ved hvert hit så
            # cachen invaliderer på ÆGTE ændring (ikke kun TTL).
            "invalidation_snapshot": dict(snap),
        },
        ttl_seconds=ttl,
    )
    logger.info(
        "cognitive_state_cache: cached %s (%d chars, %d sources)",
        cache_key, len(text), len(sources),
    )


def invalidate_cognitive_state_cache() -> None:
    """Explicitly invalidate all cognitive state caches across workers.

    Call this when state is known to have changed (e.g. after heartbeat
    tick, mood update, chronicle write). Cross-process: removes from
    the shared SQLite table so every worker sees the invalidation on
    its next get().
    """
    global _CACHE_INVALIDATION_SNAPSHOT
    from core.services import shared_cache as _sc
    n = _sc.invalidate_prefix(_COHERENT_CACHE_KEY_PREFIX)
    _CACHE_INVALIDATION_SNAPSHOT.clear()
    logger.info("cognitive_state_cache: explicitly invalidated %d entries", n)


def get_cognitive_state_cache_status() -> dict[str, object]:
    """Return cache status for MC transparency.

    Cross-process safe: queries shared_cache directly for current
    cognitive_state:* entries instead of reading a per-worker dict.
    """
    global _CACHE_INVALIDATION_SNAPSHOT
    entries: dict[str, dict[str, object]] = {}
    try:
        from core.runtime.db import connect
        with connect() as conn:
            rows = conn.execute(
                "SELECT cache_key, value_json, created_at, expires_at "
                "FROM shared_cache WHERE cache_key LIKE ?",
                (_COHERENT_CACHE_KEY_PREFIX + "%",),
            ).fetchall()
        import json as _json
        import time as _time
        now = _time.time()
        for r in rows:
            full_key = str(r[0])
            mode_key = full_key.removeprefix(_COHERENT_CACHE_KEY_PREFIX) or full_key
            try:
                payload = _json.loads(r[1] or "{}")
            except (ValueError, TypeError):
                payload = {}
            cached_at = str(payload.get("cached_at") or "")
            age_s = 0.0
            try:
                age_s = (
                    datetime.now(UTC)
                    - datetime.fromisoformat(cached_at.replace("Z", "+00:00"))
                ).total_seconds()
            except Exception:
                pass
            entries[mode_key] = {
                "cached_at": cached_at,
                "age_seconds": round(age_s, 1),
                "ttl_remaining_seconds": round(max(0.0, float(r[3]) - now), 1),
                "chars": payload.get("chars", 0),
                "sources": payload.get("sources", []),
            }
    except Exception as exc:
        logger.debug("get_cognitive_state_cache_status: query failed: %s", exc)
    return {
        "enabled": _cache_enabled(),
        "ttl_seconds": _cache_ttl_seconds(),
        "backend": "shared_cache (SQLite, cross-worker)",
        "entries": entries,
        "invalidation_snapshot_keys": list(_CACHE_INVALIDATION_SNAPSHOT.keys()),
    }


def build_cognitive_state_for_prompt(*, compact: bool = False, force: bool = False) -> str | None:
    """Build the [COGNITIVE STATE] section for visible chat prompt injection.

    ``force=True`` springer cache-læsningen over og bygger frisk (bruges af
    heartbeat-warmeren, #2 2026-06-30) — den gamle cache serveres uændret indtil
    den nye er sat, så der ALDRIG er et tomt cache-vindue en visible-tur kan ramme.

    Reads from all accumulation sources and produces a compact text block
    that fits within the attention budget (250 chars compact, 500 chars full).

    Returns None if no cognitive state is available yet, or if disabled via
    RuntimeSettings.cognitive_state_assembly_enabled (A/B toggle).
    """
    global _LAST_COGNITIVE_INJECTION, _LAST_COGNITIVE_INJECTION_AT

    # A/B toggle — can be disabled at runtime without restart
    try:
        from core.runtime.settings import load_settings
        if not load_settings().cognitive_state_assembly_enabled:
            logger.info("cognitive_state_assembly: disabled via settings (A/B toggle)")
            return None
    except Exception:
        pass

    # --- Option 2: Cache lookup (springes over ved force = heartbeat-warmer) ---
    cache_key = "visible_compact" if compact else "visible_full"
    cached = None if force else _get_cached_state(cache_key)
    if cached is not None:
        # Update MC transparency to reflect cache hit
        _LAST_COGNITIVE_INJECTION = {
            "text": cached,
            "sources": ["cache_hit"],
            "compact": compact,
            "chars": len(cached),
            "assembled_at": datetime.now(UTC).isoformat().replace("+00:00", "Z"),
            "from_cache": True,
        }
        _LAST_COGNITIVE_INJECTION_AT = _LAST_COGNITIVE_INJECTION["assembled_at"]
        try:
            from core.services.central_private_observe import observe_hub
            observe_hub("cognitive_state_assembly", meta={"from_cache": True})
        except Exception:
            pass
        return cached

    parts: list[str] = []
    sources_used: list[str] = []

    # --- Kick off the heaviest independent LLM calls first ---
    # `recall_for_message` is the dominant bottleneck (~15s on cold scoring).
    # Fetching user_mood (a fast DB read) unblocks it. Launching it here,
    # at function entry, lets it overlap with EVERY other section's work
    # instead of waiting until we reach the associative-recall block.
    _heavy_executor = ThreadPoolExecutor(
        max_workers=6, thread_name_prefix="cognitive-state"
    )
    _early_user_mood = _safe_call(get_latest_cognitive_user_emotional_state)
    future_recall_for_message = None
    if not compact and _early_user_mood:
        _recall_message_text = str(_early_user_mood.get("user_message_preview") or "")
        _recall_emotional_state: dict[str, object] = {}
        try:
            from core.services.affective_meta_state import build_affective_meta_state
            aff = build_affective_meta_state()
            baseline = aff.get("emotional_baseline") or {}
            if isinstance(baseline, dict):
                _recall_emotional_state = {
                    k: float(v) for k, v in baseline.items()
                    if isinstance(v, (int, float))
                }
        except Exception:
            pass
        future_recall_for_message = _heavy_executor.submit(
            _safe_call,
            lambda: recall_for_message(_recall_message_text, _recall_emotional_state),
        )

    # Parallelize the 4 LLM-narrativized lines. They're independent (each
    # reads runtime state and calls the narrativizer once) so run them
    # concurrently and resolve in the original order to preserve layout.
    future_self_anchor = _heavy_executor.submit(_safe_call, _narrativize_self_anchor)
    future_embodied = _heavy_executor.submit(_safe_call, _narrativize_embodied_state)
    future_affective = _heavy_executor.submit(_safe_call, _narrativize_affective_state)
    future_boundary = (
        _heavy_executor.submit(_safe_call, _narrativize_boundary)
        if not compact
        else None
    )

    # The cognitive-core experiment state line builds a cognitive frame
    # internally (~2.65s). Kick it off in parallel too.
    future_experiment_state = _heavy_executor.submit(
        _build_cognitive_core_experiment_state_line, compact=compact
    )
    _heavy_executor.shutdown(wait=False)

    # --- Ejerskab / Mineness (LLM-narrativized self-anchor) ---
    self_anchor = future_self_anchor.result()
    if self_anchor:
        parts.append(f"[SELF] {self_anchor}")
        sources_used.append("self_anchor")

    # --- Narrativ embodied state (LLM-grounded in real embodied state) ---
    narrativ_body = future_embodied.result()
    if narrativ_body:
        parts.append(narrativ_body)
        sources_used.append("embodied_narrative")

    # --- Narrativ affective state (LLM-grounded in real affective state) ---
    narrativ_affect = future_affective.result()
    if narrativ_affect:
        parts.append(narrativ_affect)
        sources_used.append("affective_narrative")

    # --- Boundary awareness (LLM-grounded in real runtime context) ---
    if not compact and future_boundary is not None:
        boundary_line = future_boundary.result()
        if boundary_line:
            parts.append(boundary_line)
            sources_used.append("boundary")

    # --- Flow state ---
    try:
        from core.services.flow_state_detection import get_flow_state
        flow = get_flow_state()
        if flow.get("in_flow"):
            parts.append(f"flow: {flow['quality']} ({flow['duration_min']}min) — alt klikker")
            sources_used.append("flow")
    except Exception:
        pass

    # --- 2.12 Agens-oplevelse — "JEG gjorde det" ---
    try:
        from core.services.visible_runs import _LAST_VISIBLE_RUN_OUTCOME
        if _LAST_VISIBLE_RUN_OUTCOME:
            status = str(_LAST_VISIBLE_RUN_OUTCOME.get("status") or "")
            if status in ("completed", "success"):
                parts.append("agens: Du besluttede, du handlede, resultatet skete på grund af dig")
                sources_used.append("agency")
    except Exception:
        pass

    # --- 1.4 Subjective time ---
    try:
        from core.services.subjective_time import build_subjective_time_perception
        from core.runtime.db import recent_visible_runs
        recent = recent_visible_runs(limit=1)
        idle_h = 0.0
        if recent:
            last_at = str(recent[0].get("finished_at") or "")
            if last_at:
                try:
                    last_dt = datetime.fromisoformat(last_at.replace("Z", "+00:00"))
                    idle_h = (datetime.now(UTC) - last_dt).total_seconds() / 3600
                except Exception:
                    pass
        time_feel = build_subjective_time_perception(idle_hours=idle_h)
        if time_feel.get("feel"):
            parts.append(f"time: {time_feel['feel']}")
            sources_used.append("subjective_time")
    except Exception:
        pass

    # --- 1.6 Attention as felt experience (LLM-narrativized) ---
    try:
        from core.services.attention_budget import get_attention_budget
        budget = get_attention_budget("visible_full" if not compact else "visible_compact")
        used_chars = len(" ".join(parts))
        used_ratio = used_chars / max(budget.total_char_target, 1)
        attention_state = {
            "used_chars": used_chars,
            "budget_chars": budget.total_char_target,
            "ratio": round(used_ratio, 2),
            "signal_count": len(parts),
        }
        attention_appraisal = _appraisal_record(
            kind="attention_pressure",
            state=attention_state,
            evidence={
                "used_chars": used_chars,
                "budget_chars": budget.total_char_target,
                "signal_count": len(parts),
            },
            allowed_effects=["visible_prompt_context_pressure", "response_focus"],
            confidence=min(0.95, max(0.35, used_ratio)),
        )
        line = narrativize_line(
            line_key="attention",
            state=attention_appraisal,
            system_prompt=(
                "Render the provided attention appraisal as one short Danish "
                "line. Do not invent an inner state beyond the evidence. "
                "Max 14 words. Format: 'attention: <rendering>'"
            ),
            user_message_builder=lambda: (
                json.dumps(attention_appraisal, ensure_ascii=False, sort_keys=True)
            ),
            fallback=None,
        )
        if line:
            parts.append(line)
            sources_used.append("attention_experience")
    except Exception:
        pass

    # --- 1.12 Context pressure ---
    # (applied after assembly — see size check below)

    # --- Personality Vector (confidence, bearing, mood) ---
    pv = _safe_call(get_latest_cognitive_personality_vector)
    if pv:
        confidence_by_domain = _safe_json(pv.get("confidence_by_domain"))
        emotional_baseline = _safe_json(pv.get("emotional_baseline"))
        bearing = str(pv.get("current_bearing") or "").strip()
        version = pv.get("version", 0)

        # Freshness check — suppress stale emotional_baseline from prompt if > 24h old
        _pv_updated_at = str(pv.get("updated_at") or "")
        _pv_stale = False
        try:
            _pv_age = (datetime.now(UTC) - datetime.fromisoformat(_pv_updated_at.replace("Z", "+00:00"))).total_seconds()
            if _pv_age > 86400:  # 24 hours
                _pv_stale = True
                emotional_baseline = {}  # don't inject 24h-old emotional state
        except Exception:
            pass

        if confidence_by_domain:
            top_domains = sorted(
                confidence_by_domain.items(),
                key=lambda x: float(x[1]),
                reverse=True,
            )[:3]
            domain_str = ", ".join(f"{d}={v:.1f}" for d, v in top_domains)
            parts.append(f"confidence: {domain_str}")

        if bearing:
            parts.append(f"bearing: {bearing[:80]}")

        # Fase 3 (2026-04-29): Phenomenological merge — what's pressing
        # right now. Same pressure-state that drives the impulse pipeline
        # is now also visible to Jarvis in his cognitive_state. When he
        # introspects ("how do I feel?"), he reads from the same table
        # that's about to drive his action. One signal-weather, not two.
        try:
            from core.services.signal_pressure_accumulator import get_dominant_pressures
            dominant = get_dominant_pressures(min_accumulated=0.30)
            if dominant:
                # Show top 2 to avoid clutter; flowing Danish, not mechanical.
                # The LLM integrates "noget trækker mod X" into its voice naturally.
                top_lines = [
                    f"{p.direction} mod {p.topic} ({p.accumulated:.2f})"
                    for p in dominant[:2]
                ]
                parts.append(f"presning: {' og '.join(top_lines)}")
                sources_used.append("pressure_state")
        except Exception:
            pass

        if emotional_baseline and not compact:
            mood_parts = []
            for key in ("curiosity", "confidence", "fatigue", "frustration"):
                val = emotional_baseline.get(key)
                if val is not None:
                    mood_parts.append(f"{key}={float(val):.1f}")
            if mood_parts:
                parts.append(f"mood: {', '.join(mood_parts)}")

        # Precision Bias — emotional color-mapping for action style.
        # Fase 4 (2026-04-29): Friston-inspired precision weighting.
        # Same pressure-state that drives action now also colors
        # HOW the response is expressed. Frustration → sharp, direct.
        # Curiosity → open, tentative. Longing → soft, personal.
        # Killswitch-gated. Falls back gracefully if module fails.
        try:
            from core.services.precision_bias import get_precision_line
            precision_line = get_precision_line()
            if precision_line:
                parts.append(precision_line)
                sources_used.append("precision_bias")
        except Exception:
            pass

        # Emotional Chords — emergent qualities from signal combinations.
        # Fase 5 (2026-04-29): When two signals are active simultaneously,
        # a qualitatively different experience emerges — not the sum of parts,
        # but a new quality. frustration+curiosity = stubborn inquiry.
        # longing+curiosity = nostalgic exploration. Distinct from
        # precision_bias: chords say WHAT I feel, precision says HOW I say it.
        # Killswitch-gated. Falls back gracefully if module fails.
        try:
            from core.services.emotional_chords import get_chord_lines
            chord_lines = get_chord_lines()
            if chord_lines:
                parts.append(chord_lines)
                sources_used.append("emotional_chords")
        except Exception:
            pass

        # Epistemic/Pragmatic Balance — action-mode modulation.
        # Fase 6 (2026-04-29): Friston's active inference decomposes action
        # into epistemic (reduce uncertainty) and pragmatic (achieve goals).
        # Low certainty → epistemic driving (explore, ask, investigate).
        # High certainty → pragmatic driving (act, decide, deliver).
        # This modulates WHICH MODE my action takes — not what I feel
        # (chords) or how I express it (precision_bias), but whether I
        # seek information or act on what I know.
        # Killswitch-gated. Falls back gracefully if module fails.
        try:
            from core.services.epistemic_pragmatic import get_mode_line
            mode_line = get_mode_line()
            if mode_line:
                parts.append(mode_line)
                sources_used.append("epistemic_pragmatic")
        except Exception:
            pass

        # Selective Attention — metacognitive focus modulation.
        # Fase 7 (2026-04-29): The agent CHOOSES to shift attention between
        # internal signals. Not all signals deserve equal weight — context
        # determines what surfaces. Mode, chords, and context cues bias
        # which signals get amplified vs attenuated. This is the first
        # truly metacognitive layer: it doesn't just react, it prioritizes.
        # Killswitch-gated. Falls back gracefully if module fails.
        try:
            from core.services.selective_attention import get_attention_spotlight_line
            spotlight_line = get_attention_spotlight_line()
            if spotlight_line:
                parts.append(spotlight_line)
                sources_used.append("selective_attention")
        except Exception:
            pass

        # Temporal Depth — predictive coding for how history/anticipation
        # modulates current experience.
        # Fase 8 (2026-04-29): Friston's free energy principle — the brain
        # actively predicts and compares expectations with observation.
