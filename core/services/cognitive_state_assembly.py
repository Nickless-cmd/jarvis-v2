def _narrativize_embodied_state() -> str | None:
    """LLM-narrativize current embodied state into a felt-experience line.

    Reads real embodied_state surface (cpu, ram, strain, primary
    state, recent transitions) and asks the local-lane LLM to render
    one short Danish sentence about how the body feels. The narrative
    only refreshes when the underlying state changes.
    """
    try:
        from core.services.embodied_state import build_embodied_state_surface
        state = build_embodied_state_surface()
    except Exception:
        return None
    if not state:
        return None

    primary = str(state.get("state") or state.get("primary_state") or "").lower()
    strain = str(state.get("strain_level") or "").lower()
    if not primary and not strain:
        return None

    fingerprint_state = {
        "primary": primary,
        "strain": strain,
        "cpu": state.get("cpu_pressure") or state.get("cpu_load"),
        "ram": state.get("ram_pressure") or state.get("ram_used"),
        "transition": state.get("recent_transition"),
    }
    appraisal = _appraisal_record(
        kind="embodied_runtime_state",
        state=fingerprint_state,
        evidence={
            "primary_state": primary,
            "strain": strain,
            "cpu": fingerprint_state["cpu"],
            "ram": fingerprint_state["ram"],
            "recent_transition": fingerprint_state["transition"],
        },
        allowed_effects=["visible_prompt_body_line", "cadence_context"],
        confidence=0.8 if primary or strain else 0.4,
    )

    return narrativize_line(
        line_key="body",
        state=appraisal,
        system_prompt=(
            "Render the embodied runtime appraisal as one short Danish line. "
            "Ground the wording only in the machine evidence: cpu, ram, strain. "
            "Max 16 words. Format: 'body: <rendering>'"
        ),
        user_message_builder=lambda: (
            json.dumps(appraisal, ensure_ascii=False, sort_keys=True)
        ),
        fallback=None,
    )


